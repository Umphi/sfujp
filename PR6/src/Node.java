/**
 * Узел двунаправленного списка.
 * Узел хранит данные и ссылки на предыдущий и следующий узлы.
 * 
 * @param <T> тип данных, хранящихся в узле
 */
class Node<T> {
    /** Данные, хранящиеся в узле */
    T data;
    
    /** Ссылка на предыдущий узел */
    Node<T> prev;
    
    /** Ссылка на следующий узел */
    Node<T> next;
    
    /**
     * Конструктор узла.
     * 
     * @param data данные для хранения в узле
     */
    Node(T data) {
        this.data = data;
        this.prev = null;
        this.next = null;
    }
}